from lib import 讀取帳密, 驗證帳密, 建立資料庫, 建立會員資料表, 讀取會員檔案, 匯入會員資料

def 主程式():
    # 步驟 1：讀取帳密
    帳密檔案路徑 = 'pass.json'
    帳密資訊 = 讀取帳密(帳密檔案路徑)
    if 帳密資訊 is None:
        return

    # 步驟 2：驗證使用者身份
    使用者名稱 = input("輸入使用者名稱：")
    密碼 = input("輸入密碼：")
    if not 驗證帳密(帳密資訊, 使用者名稱, 密碼):
        return

    # 步驟 3：建立資料庫和 'members' 資料表
    連線 = 建立資料庫()
    if 連線 is None:
        return
    建立會員資料表(連線)

    # 步驟 4：讀取並匯入會員資料
    會員檔案路徑 = 'members.txt'
    會員資料 = 讀取會員檔案(會員檔案路徑)
    if 會員資料 is not None:
        匯入會員資料(連線, 會員資料)

    # 在這裡添加選單和 CRUD 操作

    # 關閉資料庫連線
    連線.close()

if __name__ == "__main__":
    主程式()
